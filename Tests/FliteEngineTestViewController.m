- (IBAction)speech {
	NSString * text = self.textView.text;
	[engine speak:text];
}

- (IBAction)changeGender:(UISegmentedControl*)control {
    if (control.selectedSegmentIndex == 0) {
        [engine setGender:kESpeakEngineGenerMale];
    } else {
        [engine setGender:kESpeakEngineGenerFemale];
    }
}

